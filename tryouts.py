archivo = open("numeros.txt","r")
lineas = archivo.readlines()

for linea in lineas:
    t1,t2 = linea.strip().split("-")
    base1,num1 = t1.split(";")
    base2,num2 = t2.split(";")
    numeros.append((num1,base1))
    numeros.append((num2,base2))

archivo.close()

print(numeros)
